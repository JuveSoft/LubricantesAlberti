B) FILTROS DE ACEITE EN BASE A SI SE PUEDE SEPARAR O NO EL ELEMENTO FILTRANTE:
Filtros blindados.

Es el tipo de filtro de aceite más extendido en los vehículos actuales, aquí el elemento filtrante no se puede separar
de la carcasa por eso de les llama blindados, por lo que cuando cambiamos este tipo de filtros se cambia todo , de hecho
en la propia carcasa llevan una rosca para fijarlos directamente al motor.

La carcasa suele estar hecha de metal o acero

Filtros de cartucho.

En este tipo de filtros son básicamente el elemento filtrante, se les llama de cartucho porque tiene forma cilíndrica y
un orificio que recorre en vertical el filtro.

Aquí la carcasa del filtro ya está fijada en el propio carro, por lo que lo que hay que hacer es abrir la tapa y poner
dentro el filtro de cartucho.

Algunas marcas actuales de vehículos montan en algunos de sus modelos este tipo de filtros, pero representan el 5%
aproximadamente de la totalidad de filtros del mercado.
¿Cómo funciona?
Cuando el motor se encuentra apagado, el aceite se aloja en cárter de aceite. Al poner el motor en funcionamiento, una
bomba hace circular el aceite por todo el sistema a fin de lubricar todos los componentes.

Antes de llegar a la parte superior del motor, el aceite pasa por el filtro que se encarga de limpiarlo para evitar
fallas por falta de presión por alguna obstrucción o exceso de fricción entre las partes gracias a las impurezas y
residuos.

