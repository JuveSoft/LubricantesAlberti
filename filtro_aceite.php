<p>¿Alguna vez se ha preguntado por qué su filtro de aceite necesita ser reemplazado junto con el aceite a intervalos
especificados que son recomendados por el fabricante de OE? Hemos creado unas preguntas frecuentes rápidas para ayudarle
a comprender mejor la importancia del mantenimiento regular y el uso de piezas correctas para prolongar la vida útil de
su motor y coche.

¿Qué hace un filtro de aceite?

El filtro de aceite de coche elimina constantemente la suciedad y las impurezas del aceite que circula alrededor de las
piezas móviles del motor. El aceite limpio proporciona una mayor lubricación y reduce la acumulación de calor debido a
la fricción dentro del motor. Su filtro de aceite debe cambiarse cada vez que cambie el aceite a medida que el medio
recoge la suciedad y los escombros a medida que acumula las millas en su viaje.

¿Cómo se construye el filtro de aceite?

Su filtro de aceite suele ser de forma cilíndrica. La caja exterior está hecha de un metal delgado. En el interior, hay
un medio de fibra como algodón a granel o papel de filtro plisado, que es lo que hace el trabajo. El medio se mantiene
dentro del filtro mediante una pieza metálica perforada que permite que el aceite fluya dentro y fuera. Se atornilla en
su lugar por lo general en el lado o cerca de la parte inferior del motor.

¿Cómo protege el aceite el motor?

El aceite se refiere a menudo al alma de un motor. En realidad, es más como un amortiguador líquido. Cada parte móvil
del motor se frota constantemente contra otras piezas metálicas, incluyendo los pistones, levas, varillas de empuje y
baleros. El aceite proporciona una capa protectora entre los componentes. Cuando el peso correcto y el aceite
correctamente filtrado está bombeando a través de todo el sistema, hay menos fricción y una tasa mucho menor de metal
llamativo metal, aumentando la vida útil del motor y manteniendo el rendimiento.

¿Qué sucede si se instala el filtro de aceite incorrecto?

Puede ser tentador recoger un filtro de aceite diseñado para un coche que no es del mismo modelo que el suyo porque está
a la venta. Sin embargo, el uso del filtro de aceite incorrecto puede conducir a algunos problemas graves. Si lo hace,
puede resultar en:

• Fuga de aceite alrededor del soporte del filtro,

• Baja presión de aceite,

• Alta presión de aceite,

• Suciedad que entra en el sistema de aceite a través de accesorios sueltos,

• Daños a los componentes del motor y/o

• Sobrecalentamiento.

Cada pieza de su motor fue diseñada para trabajar entre sí. Cada vez que agregas una pieza que pertenece a un coche
diferente, aumentas la probabilidad de daño o fallo de pieza.

Qué buscar al comprar un filtro de aceite automotriz de reemplazo

Si bien no tiene que comprar el auténtico reemplazo de filtro automotriz vendido en su concesionario, es muy importante
comprar uno que cumpla con las especificaciones OEM del filtro original. Haga una referencia cruzada de la marca y el
modelo de su coche junto con su tamaño de motor con respecto a la lista de filtros ofrecidos por un fabricante de
filtros. Compruebe que el filtro está diseñado para durar durante la duración esperada de su cambio de aceite, ya sea de
5,000 o 16.000 Km. Mientras que un filtro se adapta a una variedad de vehículos, ninguno de los filtros automotrices
disponibles se adaptará a todos los automóviles.
¿QUÉ ES EL FILTRO DE ACEITE DEL MOTOR?
El filtro de aceite es un órgano vital en el funcionamiento del motor.

Su principal misión es retener todos los residuos metálicos y contaminantes que se van generando en el aceite del motor

Dichos residuos se van acumulando en el sistema de lubricación por el normal funcionamiento del motor y que la
combustión siempre genera contaminantes que en mayor o menor grado van a parar al aceite de nuestro vehiculo.

El filtro de aceite es un sistema de filtración increíble porque por un lado impide que estas impurezas pasen al
circuito de lubricación dañando elementos del motor y por otro lado no restringe el flujo de aceite al motor, por lo que
durante su funcionamiento el aceite seguirá fluyendo y protegiendo todos los componentes móviles frente al desgaste.
¿CUÁL ES LA FUNCIÓN DE LOS FILTROS DE ACEITE EN LOS MOTORES DE LOS COCHES?
Como hemos comentado el filtro recoge cualquier impureza del aceite del motor antes de que pase al sistema de
lubricación de nuestro vehículo.

Las principales funciones del filtro de aceite serían las siguientes:

Reducir la fricción de las partes móviles del motor al evitar el paso de impurezas abrasivas en el circuito de
lubricación.

Al reducir la fricción el motor reduce su desgaste, aumenta su vida útil y el motor no pierde potencia.

Disipa el calor evitando excesos de temperatura.

Garantizar el suministro optimo del motor sin importar las altas temperaturas, alta densidad del aceite en frío, que
pase combustible al aceite, los picos de presión etc.

Sin el filtro de aceite la vida del motor se reduciría drásticamente.

TIPOS DE FILTRO DE ACEITE MOTOR
Vamos a mostrar dos tipos de clasificaciones:

A) FILTROS DE ACEITE EN BASE A LA FORMA DE FILTRAR EL ACEITE:
En la actualidad hay 3 tipos de sistemas de filtración en base a la forma de filtrar:

1. Sistema de filtración de aceite de tipo flujo parcial o by-pass.

Este sistema de filtración está bastante desfasado porque es de los menos eficientes, se trata de uno de los sistemas
más antiguos, aquí solo una pequeña parte del aceite pasa por filtro, un 15% aproximadamente, el 85% restante pasa al al
motor sin filtrar.

Se le llama by-pass porque de la bomba sale un desvio que lleva una pequeña parte del aceite al filtro y del filtro al
motor.

Muy pocos carros en la actualidad emplean este método.

2. Sistema de filtración de aceite de tipo flujo total.

Este tipo de sistema de filtración es el más utilizado por los carros en la actualidad, ya que es el sistema más
eficiente para proteger el motor, ya que la totalidad del aceite es filtrado antes de pasar al motor. Garantiza un alto
nivel de limpieza

Durante su funcionamiento el filtro cada vez se irá obstruyendo cada vez más, por lo que la presión del aceite
disminuirá pudiendo llegar a niveles peligrosos, por esta razón casi todos los filtros de flujo total llevan una válvula
de alivio o de seguridad, para que cuando el filtro esté saturado se abra una válvula y deje correr el aceite sin
filtrar, porque es preferible que circule aceite sucio en el motor a que no circule aceite, ya que esta falta de aceite
dañaria el motor.

3. Sistema de filtración de flujo combinado.

Este sistema debido al espacio que ocupa no lo suelen llevar los carros convencionales. Lo pueden llevar grandes motores
diésel como podría ser un camión, autobús o cualquier motor grande y pesado.

Es una combinación de los dos sistemas anteriores, ya que consta de dos filtros, tiene un filtro principal de flujo
total y un filtro secundario de derivación (o by pass), además estos dos filtros funcionan de forma independiente.

Este tipo de filtro combinado es el más efectivo para retener grandes cantidades de contaminantes.
